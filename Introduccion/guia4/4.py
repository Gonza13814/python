"""4) esVocal, que devuelva si un caracter dado como parámetro es o no una letra vocal (contemplar mayúsculas y minúsculas). """

def esVocal(letra):
    return (letra == "a" or letra == "A" or letra == "e" or letra == "E" or letra == "i" or letra == "I" or letra == "o" or letra == "O" or letra == "u" or letra == "U")



print(esVocal('a'))



